def find_bad_images(folder):
    bad_files = []

    for root, dirs, files in os.walk(folder):
        for file in files:
            path = os.path.join(root, file)
            try:
                img = Image.open(path)
                img.verify()  # verify image
            except:
                bad_files.append(path)

    return bad_files

